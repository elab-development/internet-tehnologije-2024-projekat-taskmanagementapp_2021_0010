use App\Models\TaskList;
class TaskListSeeder extends Seeder
{
    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $users = User::all();
        foreach ($users as $user) {
            TaskList::factory()->count(3)->create([
                'user_id' => $user->id,
            ]);
        }
    }
}
